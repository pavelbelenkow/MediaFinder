import Foundation

extension NSNumber {
    
    /// Stores default shimmer locations `[0.0, 0.5, 1.0]`
    static let defaultShimmerLocations: [NSNumber] = [0.0, 0.5, 1.0]
    
    /// Stores default shimmer from values `[-1.0, -0.5, 0.0]`
    static let defaultShimmerFromValues: [NSNumber] = [-1.0, -0.5, 0.0]
    
    /// Stores default shimmer to values `[1.0, 1.5, 2.0]`
    static let defaultShimmerToValues: [NSNumber] = [1.0, 1.5, 2.0]
}
